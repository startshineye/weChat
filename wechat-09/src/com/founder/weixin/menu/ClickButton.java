package com.founder.weixin.menu;
/**
 * click类型按钮
 * @author yxm
 * @date 2016-08-09
 */
public class ClickButton extends Button{
	private String type;
	private String key;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}
}
